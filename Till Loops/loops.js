// There are :

// For, while, Do while, for in, for of

// For :

for(let i = 1; i <= 100; i++){
    console.log(i);
}


//While :
let i = 1
while(i<=100){
    console.log(i);
    i++;
}


// Do while :

let k = 6;
do{
    console.log(k);
}
while(k<5);


// For Loop :
for(let j = 1; j<=20;j++){
    console.log(j)
}