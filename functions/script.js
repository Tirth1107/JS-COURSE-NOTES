// Ways To make Functions :

// 1: Function Expression :  Here the variable name is function only
let x = function(){
    console.log("Variable Function")
}
x();

// 2: Function Statement : The normal way
function y(){
    console.log("Function Statement")
}
y();

// Arrow/Fat arrow function 
let z = ()=>{
    console.log("Fat Arrow Function");
}
z();

